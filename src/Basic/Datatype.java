class Datatype
{
	public static void main(String args[])
	{
		int a=10;
		float f=2.24f;
		double d=2.4556;
		char ch='a';
		Boolean b=true;
		System.out.println("a="+a);
		System.out.println("f="+f);
		System.out.println("d="+d);
		System.out.println("ch="+ch);
		System.out.println("b="+b);

	}

}
